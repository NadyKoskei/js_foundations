//ES6 Features
// LET and CONST are two ways to declare variables in JavaScript, introduced in ES6.
// LET allows you to declare block-scoped variables, meaning the variable is only accessible within the block it is defined in.
let age = 25;
console.log("Age:", age);
// CONST is used to declare variables that cannot be reassigned after their initial assignment. It is also block-scoped.
const birthYear = 1999;
console.log("Birth Year:", birthYear);

